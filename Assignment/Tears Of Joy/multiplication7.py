"""
using for loop to print multiplication table of 7. take number = 7 and count in range 1 - 13,so it can start from 1 and end at 12. Then multiply number by count,then print the result as number * count = multiply



"""

number = 7
for count in range(1,13):
    multiply = number * count
    print (number,"X",count, "=", multiply)
       
